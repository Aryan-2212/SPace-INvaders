import pygame

class Block(pygame.sprite.Sprite):
    """Represents a single block in the obstacle."""
    def __init__(self, x, y):
        super().__init__()
        # Create a small rectangular surface for the block
        self.image = pygame.Surface((3, 3))  # Each block is 3x3 pixels
        self.image.fill((243, 216, 63))  # Fill the block with a yellow color
        # Position the block at the specified (x, y) coordinates
        self.rect = self.image.get_rect(topleft=(x, y))

# Define the layout for the obstacle using a 2D grid
