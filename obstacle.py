class Obstacle:
    """Represents a group of blocks forming an obstacle."""
    def __init__(self, x, y):
        # Group to hold all blocks in the obstacle
        self.blocks_group = pygame.sprite.Group()

        # Loop through the grid and create blocks for each "1" in the grid
        for row in range(len(grid)):  # Iterate through rows of the grid
            for column in range(len(grid[0])):  # Iterate through columns of the grid
                if grid[row][column] == 1:  # If grid cell is "1", create a block
                    pos_x = x + column * 3  # Calculate the x-coordinate
                    pos_y = y + row * 3  # Calculate the y-coordinate
                    block = Block(pos_x, pos_y)  # Create a block at the calculated position
                    self.blocks_group.add(block)  # Add block to the group
